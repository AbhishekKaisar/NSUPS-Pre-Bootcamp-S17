#include <iostream>

int main() {
    // Declare variables to store the two integers
    int num1, num2;

    // Read the first integer from stdin
    std::cin >> num1;

    // Read the second integer from stdin
    std::cin >> num2;

    // Calculate the sum
    int sum_result = num1 + num2;

    // Print the sum to stdout
    std::cout << sum_result << std::endl;

    return 0;
}
